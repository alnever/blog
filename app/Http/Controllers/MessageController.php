     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
      $message = Message::find($id);
      $message->delete();

      Session::flash('success','The message was successfully deleted.');

